use std::{
    io::{self, Write},
    thread,
    time::Duration,
};

fn type_text(text: &str, delay_ms: u64) {
    for ch in text.chars() {
        print!("{ch}");
        io::stdout().flush().unwrap();
        thread::sleep(Duration::from_millis(delay_ms));
    }
    println!();
}

